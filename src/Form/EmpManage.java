    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int rowclick = tbEmp.getSelectedRow();
            
            if(rowclick != -1){
               if(JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Warning!: Action required", 
                                                JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0){
                   
                   sql = "delete from tb_employee where EmpId=?";
                   
                   psm = c.prepareStatement(sql);
                   psm.setInt(1, Integer.parseInt(model.getValueAt(rowclick, 0).toString()));
                  
                   if(psm.executeUpdate() != -1){
                       showData();
                   }
               }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

