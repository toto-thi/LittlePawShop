    public void showData(){
        try {
            int rowidx = tbEmp.getRowCount()-1;
            
            while(rowidx>-1){
                model.removeRow(rowidx--);
            }
            sql = "select * from tb_employee";
            
            rs = c.createStatement().executeQuery(sql);
            
            while(rs.next()){
                String [] data = {rs.getString("EmpId"),
                                  rs.getString("EmpName"),
                                  rs.getString("EmpSurname"),
                                  rs.getString("Gender"),
                                  rs.getString("DOB"),
                                  rs.getString("Village"),
                                  rs.getString("City"),
                                  rs.getString("Province"),
                                  rs.getString("Tel"),
                                  rs.getString("AccountType")};
                model.addRow(data);
            }
            rs.close();
            tbEmp.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
