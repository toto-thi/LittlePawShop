    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            sql = "UPDATE tb_employee SET  EmpName=?, EmpSurame=?, Gender=?, DOB=?, Village=?, City=?, Province=?, Tel=?, AccountType=? where EmpId=?";;

            psm = c.prepareStatement(sql);
            psm.setString(1, txtName.getText());
            psm.setString(2, txtSurname.getText());

            String g = "";
            g = (rbMale.isSelected())?"Male":"Female";
            psm.setString(3, g);
            psm.setString(4, new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate()));
            psm.setString(5, txtVillage.getText());
            psm.setString(6, txtCity.getText());
            psm.setString(7, txtProvince.getText());
            psm.setString(8, txtTel.getText());

            String acc = "";
            acc = (rbAdmin.isSelected())?"Admin":"User";
            psm.setString(9, acc);

            psm.setInt(10, Integer.parseInt(txtEmpID.getText()));
            ByteArrayOutputStream out = new ByteArrayOutputStream();
            ImageIO.write(bm, "jpg", out);
            byte [] pic = out.toByteArray();
            psm.setBytes(10, pic); 

           // psm.setString(11, txtUser.getText());
           // psm.setString(12, txtPw.getText());

            if(psm.executeUpdate() != -1){
                JOptionPane.showMessageDialog(this, "Saved!", "blabla", JOptionPane.INFORMATION_MESSAGE);
                //emp.showData();

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

