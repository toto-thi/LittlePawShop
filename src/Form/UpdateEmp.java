    private void btnAddPicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPicActionPerformed
        try {
            JFileChooser fs = new JFileChooser();

            int r = fs.showOpenDialog(null);

            if(r==0){
                path = fs.getSelectedFile().getAbsolutePath();
                icon1 = new ImageIcon(path);
                im = icon1.getImage();
                bm = new BufferedImage(lbAddPic.getWidth(), lbAddPic.getHeight(), Image.SCALE_SMOOTH);
                bm.createGraphics().drawImage(im,0,0, lbAddPic.getWidth(), lbAddPic.getHeight(), null);
                icon2 = new ImageIcon(bm);

                lbAddPic.setIcon(icon2);
            }else if(r==1){
                return;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddPicActionPerformed

