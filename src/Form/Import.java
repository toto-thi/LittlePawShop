    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        try {
             sql = "insert into tb_import values(0,?,?,?,?,?)";
            psm = c.prepareStatement(sql);
            psm.setString(1, new SimpleDateFormat("yyyy-MM-dd").format(DCImport.getDate()));
            psm.setInt(2, Integer.parseInt(txtQty.getText()));
            psm.setInt(3, Integer.parseInt(txtProductID.getText()));
            psm.setString(4, new SimpleDateFormat("yyyy-MM-dd").format(DCExp.getDate()));
            psm.setInt(5, Integer.parseInt(Sup.get(cbSupp.getSelectedIndex())));
            

            if(psm.executeUpdate() != -1){
                sql = "UPDATE tb_product SET StockQty=(StockQty+?) where PId=?";
                psm = c.prepareStatement(sql);
                psm.setString(1, txtQty.getText());
                psm.setInt(2, Integer.parseInt(txtProductID.getText()));
                psm.executeUpdate();
                JOptionPane.showMessageDialog(null, "Done!","", JOptionPane.INFORMATION_MESSAGE);
                txtQty.setText("");
                DCImport.setDate(null);
                DCExp.setDate(null);
                showProductImport("");
            }          
            pm.showProduct("");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnImportActionPerformed

