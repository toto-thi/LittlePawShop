        public void cbSup(){
        try {
            sql = "select * from tb_supplier";
            
            cbSupp.removeAllItems();
            Sup = new ArrayList<String>();
            rs = c.createStatement().executeQuery(sql);
            
            while (rs.next()){
                    cbSupp.addItem(rs.getString("SName"));
                    Sup.add(rs.getString("SId"));
            }
            cbSupp.addItem("Custom Supplier");
  
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
         public void showProductImport(String search){
        try {
            model.getDataVector().removeAllElements();
            if (search.equals("")) {

                sql = "SELECT tb_import.ImpId, tb_import.ImpDate, tb_import.Quantity, "
                        + "tb_product.PId, tb_product.PName, tb_import.ExpirationDate, tb_supplier.SId "
                        + "FROM tb_import INNER JOIN tb_product ON tb_product.PId = tb_import.ProId "
                        + "INNER JOIN tb_supplier ON tb_supplier.SId = tb_import.SuppId";
            } else {
                sql = "SELECT tb_import.ImpId, tb_import.ImpDate, tb_import.Quantity, "
                        + "tb_product.PId,tb_product.PName, tb_import.ExpirationDate, tb_supplier.SId "
                        + "FROM tb_import INNER JOIN tb_product ON tb_product.PId = tb_import.ProId "
                        + "INNER JOIN tb_supplier ON tb_supplier.SId = tb_import.SuppId"
                        + " where PId Like '%" + txtSearch.getText() + "%' or "
                        + "PName Like '%" + txtSearch.getText() + "%' or "
                        + "ImpDate Like '%" + txtSearch.getText() + "%' or "
                        + "ExpirationDate Like '%" + txtSearch.getText() + "%' or "
                        + "SId Like '%" + txtSearch.getText() + "%' or "
                        + "Quantity Like '%" + txtSearch.getText() + "%'";
            }
            rs = c.createStatement().executeQuery(sql);
            
            while(rs.next()){
                String [] data = {rs.getString("ImpId"),
                rs.getString("ImpDate"),
                rs.getString("Quantity"),
                rs.getString("PId"),
                rs.getString("PName"),
                rs.getString("ExpirationDate"),
                rs.getString("SId")};
 
                model.addRow(data);
            }
            rs.close();
            tbShowProductImport.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
