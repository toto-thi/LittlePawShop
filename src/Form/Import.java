    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            sql = "delete from tb_import where ImpId=?";

            psm = c.prepareStatement(sql);
            psm.setInt(1, Integer.parseInt(tbShowProductImport.getValueAt(tbShowProductImport.getSelectedRow(), 0).toString()));
            

            if(psm.executeUpdate() != -1){
                sql = "UPDATE tb_product SET StockQty=(StockQty-?) where PId=?";
                psm = c.prepareStatement(sql);
                psm.setString(1, txtQty.getText());
                psm.setInt(2, Integer.parseInt(txtProductID.getText()));
                psm.executeUpdate();
                JOptionPane.showMessageDialog(this, "Deleted this product!", "Action Done!", JOptionPane.INFORMATION_MESSAGE);
                showProductImport("");
                    pm.showProduct("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

